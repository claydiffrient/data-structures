/*******************************************************************************
* Header file containing Node class declarations.
*******************************************************************************/
#ifndef NODE_T
#define NODE_T


template<class T>
class Node
{
   private:
      T mValue;         //Value of the Node.
      Node<T>* mNext;   //Pointer to the next node.
   public:
      /*************************************************************************
      * Default constructor.
      *************************************************************************/
      Node();
      /*************************************************************************
      * Default destructor
      *************************************************************************/
      ~Node();
      /*************************************************************************
      * getValue
      * Returns the value
      *************************************************************************/
      T getValue();
      /*************************************************************************
      * setValue
      * Sets the node's value to the given value
      *************************************************************************/
      void setValue(T value);
      /*************************************************************************
      * getNext
      * Returns a pointer to the next node.
      *************************************************************************/
      Node<T>* getNext();
      /*************************************************************************
      * setNext
      * Sets the next node pointer to the given node.
      *************************************************************************/
      void setNext(Node* next);
};

#endif